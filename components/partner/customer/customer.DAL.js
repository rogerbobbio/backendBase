const pool = require('../../../database/mysqlpool');

function createCustomer(lastname, names, address, phone, document, document_type, email, status, user_create) {    
    return new Promise(( resolve, reject ) => {
        pool.getConnection( (err, connection) => {
         if (err) {
          reject( err )
         } else {
            var values = {lastname, names, address, phone, document, document_type, email, status, user_create};
            var sql = 'INSERT INTO customer SET ?';
          connection.query(sql, values, ( err, rows) => {
           if ( err ) {
            reject( err )
           } else {
            resolve( rows )
           }
           connection.release()
          })
         }
        })
       })
}

